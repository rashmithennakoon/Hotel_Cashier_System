        String payment = jTextField1.getText();
        String breakfast = "No";
        if(jCheckBox3.isSelected()){
            breakfast = "Yes";
        }
        String lunch = "No";
        if(jCheckBox1.isSelected()){
            lunch = "Yes";
        }
        String dinner = "No";
        if(jCheckBox2.isSelected()){
            dinner = "Yes";
        }
        String meals = (String) jComboBox1.getSelectedItem();
        SimpleDateFormat acf = new
        SimpleDateFormat("yyyy-MM-dd");
