    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nic = jTextField2.getText();
        try{
                Class.forName("com.mysql.jdbc.Driver");
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/resturant_database?useSSL=false","root","");
                Statement stmt = conn.createStatement();
                String sql = "Select*from breakfast_table where nic = '"+nic+"'";
                ResultSet rs = stmt.executeQuery(sql);
                boolean isNoResult = true;
                while(rs.next()){
                    isNoResult = false;
                    stuId = rs.getString("NIC");
                    jTextField3.setText(rs.getString("Number_of_Family_Mem"));
                    jComboBox1.setSelectedItem(rs.getString("Order_Meals"));
                    jTextField1.setText(rs.getString("Payment"));
                    SimpleDateFormat dcn = new
                    SimpleDateFormat("yyyy-MM-dd");
                    jDateChooser1.setDate(dcn.parse(rs.getString("Date")));
                    String breakfast = rs.getString("Breakfast");
                    if(breakfast.equals("Yes")){
                        jCheckBox3.setSelected(true);
                    }
                    String lunch = rs.getString("Lunch");
                    if(lunch.equals("Yes")){
                        jCheckBox1.setSelected(true);
                    }
                    String dinner = rs.getString("Dinner");
                    if(dinner.equals("Yes")){
                        jCheckBox2.setSelected(true);
                    }
                }
                conn.close();
                if(isNoResult){
                    JOptionPane.showMessageDialog(null, "No Data from this NIC!");
                }else{
                    
                    jButton4.setEnabled(true);
                    jButton3.setEnabled(true);
                }
                
                
            }catch(Exception e){
                e.printStackTrace();
            }
    }//GEN-LAST:event_jButton1ActionPerformed

